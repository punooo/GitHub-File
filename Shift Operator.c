#include <stdio.h>

int main(void)
{
	int x = -8;			// 11111111 11111111 11111111 11111000
	int y = x>>2;		// 오른쪽으로 2비트만큼 이동시킴.
	int z = x>>3;		// 오른쪽으로 3비트만큼 이동시킴.
	
	printf("%d\n", y);	// 11111111 11111111 11111111 11111110 : -2
	printf("%d", z);	// 11111111 11111111 11111111 11111111 : -1
	return 0;
}